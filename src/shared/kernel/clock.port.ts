/**
 * SHARED KERNEL — Clock Port
 *
 * Clock определяет контракт получения текущего времени.
 * Абстракция повышает тестопригодность и управляемость логики, зависящей от времени.
 *
 * Ключевые принципы:
 * - Внедрение времени через DI
 * - Контролируемое время в тестах
 * - Единый источник истины по времени в приложении
 * - Возможность мокать для детерминированных тестов
 *
 * Важно для:
 * - Domain Events (occurredAt)
 * - Бизнес-логики, зависящей от времени
 * - Тестирования time-sensitive операций
 */
export interface Clock {
  /** Текущее дата-время */
  now(): Date;
}

/** DI-токен для Clock */
export const CLOCK = Symbol("CLOCK");
